int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int ret;

	ret = 0;
	while ((n != '\0') && (*s1 != '\0') && (*s1 == *s2))
	{
		s1++;
		s2++;
		n--;
	}
	if (n != 0)
	{
		ret = (*s1 - *s2);
	}
	return (ret);
}